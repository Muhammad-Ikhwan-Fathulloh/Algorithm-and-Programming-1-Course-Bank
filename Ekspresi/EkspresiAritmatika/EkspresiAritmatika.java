int a = 5;
int b = 3;
        
// Penjumlahan
int hasilPenjumlahan = a + b;
System.out.println("Hasil Penjumlahan: " + hasilPenjumlahan); // Hasil Penjumlahan: 8

// Pengurangan
int hasilPengurangan = a - b;
System.out.println("Hasil Pengurangan: " + hasilPengurangan); // Hasil Pengurangan: 2

// Perkalian
int hasilPerkalian = a * b;
System.out.println("Hasil Perkalian: " + hasilPerkalian); // Hasil Perkalian: 15

// Pembagian
double hasilPembagian = (double) a / b; // Menggunakan cast untuk hasil desimal
System.out.println("Hasil Pembagian: " + hasilPembagian); // Hasil Pembagian: 1.6666666666666667

// Modulus (Sisa Pembagian)
int hasilModulus = a % b;
System.out.println("Hasil Modulus: " + hasilModulus); // Hasil Modulus: 2
