int a = 5;
int b = 3;
        
// Penjumlahan
int hasilPenjumlahan = a + b;
System.out.println("Hasil Penjumlahan: " + hasilPenjumlahan);

// Pengurangan
int hasilPengurangan = a - b;
System.out.println("Hasil Pengurangan: " + hasilPengurangan);

// Perkalian
int hasilPerkalian = a * b;
System.out.println("Hasil Perkalian: " + hasilPerkalian);

// Pembagian
double hasilPembagian = (double) a / b; // Menggunakan cast untuk hasil desimal
System.out.println("Hasil Pembagian: " + hasilPembagian);

// Modulus (Sisa Pembagian)
int hasilModulus = a % b;
System.out.println("Hasil Modulus: " + hasilModulus);

// Hasil Penjumlahan: 8
// Hasil Pengurangan: 2
// Hasil Perkalian: 15
// Hasil Pembagian: 1.6666666666666667
// Hasil Modulus: 2
