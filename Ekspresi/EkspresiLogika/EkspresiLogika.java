int x = 5;
int y = 10;

// Operator Perbandingan
boolean lebihBesar = x > y;
boolean lebihKecil = x < y;
boolean samaDengan = x == y;

System.out.println("Apakah x lebih besar dari y? " + lebihBesar);
System.out.println("Apakah x lebih kecil dari y? " + lebihKecil);
System.out.println("Apakah x sama dengan y? " + samaDengan);

// Operator Logika
boolean hasilLogika = (x > 3) && (y < 15);
System.out.println("Hasil dari (x > 3) && (y < 15): " + hasilLogika);

// Apakah x lebih besar dari y? false
// Apakah x lebih kecil dari y? true
// Apakah x sama dengan y? false
// Hasil dari (x > 3) && (y < 15): true